module.exports = function(grunt) {
    'use strict';
    /* -----------------------*/
    // logstart
    /* -----------------------*/
    grunt.registerTask('logstart', 'ログ出力', function() {
        grunt.log.writeln('');
        grunt.log.writeln('----------------------------');
        grunt.log.writeln('　スタート！');
        grunt.log.writeln('----------------------------');
        grunt.log.writeln('');
    });
    /* -----------------------*/
    // logfinish
    /* -----------------------*/
    grunt.registerTask('logfinish', 'ログ出力', function() {
        grunt.log.writeln('');
        grunt.log.writeln('----------------------------');
        grunt.log.writeln('　フィニッシュ！');
        grunt.log.writeln('----------------------------');
        grunt.log.writeln('');
    });

};